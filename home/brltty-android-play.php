<p>
BRLTTY can be installed from Google Play.
This is what we recommend because, doing it this way, you'll automatically get new releases, important fixes, etc.
You can either search for it by name - i.e. for <q>brltty</q> (without the quotes) -
or go directly to its App page:
</p>

<ul>
<li><?php write_link("https://play.google.com/store/apps/details?id=org.a11y.brltty.android"); ?></li>
</ul>

<p>
If BRLTTY has already been installed the old way, i.e. by having sideloaded it,
then Google Play will neither automatically update it nor give you the option to reinstall it from there.
This is because there's an incompatibility between the two versions -
the Google Play version is a signed, release variant of the app
whereas the sideload version is an unsigned, debug variant of the app.
If you'd like to switch to the Google Play version then you'll need to first uninstall the sideload version.
</p>
