<B>BRLTTY</B> has been tested on an incredible range of
computers,
Linux distributions,
and kernel versions.
It runs with a good response time on a machine as pitiful as a 386SX20 with 4MB RAM.
It has been used on
laptops,
desktop PCs,
a DEC Alpha running Alpha Linux,
and a few Linux-based PDAs.
<P>
Kernel 1.1.92 or newer is required.
2.0.x kernels work fine.
<P>
<B>BRLTTY</B> does not depend on a specific distribution of Linux.
It has been installed on top of
<A HREF="http://www.debian.org/">Debian</A>,
<A HREF="http://www.knopper.net/knoppix/index-en.html">Knoppix</A>,
<A HREF="http://www.mandrakelinux.com/en">Mandrake</A>,
<A HREF="http://www.redhat.com/">Red Hat</A>,
<A HREF="http://www.slackware.com/">SlackWare</A>,
and <A HREF="http://www.suse.de/">SuSE</A>.
It has also been used in the barest of boot/root disk sets.
<P>
<B>BRLTTY</B> needs to run as <CODE>root</CODE>
in order to gain access to some of the console functions.
It runs as a daemon,
and does not need to be <CODE>set-uid</CODE> or <CODE>set-gid</CODE>.
