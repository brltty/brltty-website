<B>BRLTTY</B> cannot be used on its own within graphical environments like
<A HREF="http://www.xfree86.org/">X-Windows</A>
and
<A HREF="http://www.svgalib.org/">svgalib</A>.
Screen readers for these environments can, however,
communicate with <B>BRLTTY</B> via its <B>BrlAPI</B> interface.
<A HREF="http://live.gnome.org/Orca/Braille">Orca</A>
is an example of just such a screen reader
which uses <B>BRLTTY</B> via <B>BrlAPI</B>
in order to make the
<A HREF="http://www.xfree86.org/">X-Windows</A>
environment accessible to braille users.
