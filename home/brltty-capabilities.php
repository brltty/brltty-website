The <B>BRLTTY</B> daemon can be launched at system startup,
thus it's always on to assist you when logging in,
and allows the reading of console messages.
<P>
<B>BRLTTY</B> allows full use of the virtual terminal feature of the Linux console driver.
Blind users will discover that using several virtual terminals is very effective.
<P>
<B>BRLTTY</B> can handle those video modes
which offer more columns and/or rows than 80x25.
<P>
<B>BRLTTY</B> is adequate for performing system administration tasks.
